package Q2;

public final class CellCoordinates {
	//Variables
	private final int col_;
	private final int row_;
	
	//getCol function
	public int getCol() {
		return col_;
	}
	
	//getRow function
	public int getRow() {
		return row_;
	}
	
	//Parameter Constructor
	public CellCoordinates(int row, int col) {
		col_ = col;
		row_ = row;
	}
	
}

